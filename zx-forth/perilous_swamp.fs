( FLAG = 0 => PLAYER DEAD ; 1 = MONSTER DEAD )
    BEGIN
	GETCOMBAT ( TREASURE MSTRENGTH PSTRENGTH )
	STRENGTH @ OVER - STRENGTH ! ( DEDUCT FROM STRENGTH )

	FIGHT

	( CHECK OUTCOME )
	DUP 900 > IF
	    ." YOU SURE SMASHED THAT MONSTER" CR
	    DROP 1
	ELSE DUP 700 > IF
		." LUCKY HE WAS NOT AT ALL WELL" CR
		DROP 1
	    ELSE DUP 0 > IF
		    ." HE WONT GIVE YOU HIS TREASURE" CR
		    DROP
		    2 / ( DIVIDE MONSTER'S STRENGTH BY TWO )
		    0
		ELSE ( DEAD )
		    DROP -1
		THEN
	    THEN
	THEN
    ?DUP UNTIL

    1+ 2 / ( MAP ONTO 0,1 )

    DUP IF
	KILLS @ 1+ KILLS !
    THEN
